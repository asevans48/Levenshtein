package com.hygenics.exceptions;

/**
 * Invalid 509 Exceptions
 * 
 * @author aevans
 *
 */
public class Invalid509Exception extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 9068679317430438856L;

	/**
	 * The X509 Exception for an Invalid Cert
	 */

	public Invalid509Exception() {
		// the invalid certification
		super(
				"X509 Authority or Principal Could Not be Verified. \n Please check the information or certificate provided. \n This is Used To Prevent Attack.");
	}

	public Invalid509Exception(String message) {
		// the invalid certification
		super(
				"X509 Authority or Principal Could Not be Verified. \n Please check the information or certificate provided. \n This is Used To Prevent Attack. "
						+ message);
	}
}
